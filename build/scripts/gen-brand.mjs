import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..", "..");
const BRAND_YML = path.join(ROOT, "build", "brand.yml");
const CONFIG_YML = path.join(ROOT, "build", "config.yml");
const TASKFILE = path.join(ROOT, "Taskfile.yml");
const GO_OUT = path.join(ROOT, "internal", "brand", "brand_gen.go");
const TS_OUT = path.join(ROOT, "frontend", "src", "lib", "brand.gen.ts");
const MSIX_MANIFEST = path.join(ROOT, "build", "windows", "msix", "app_manifest.xml");

const REQUIRED_KEYS = [
  "company",
  "displayName",
  "slug",
  "bundleId",
  "description",
  "tagline",
  "comments",
  "copyright",
  "version",
  "homepage",
  "dataDir",
  "dbFile",
  "envPrefix",
  "localStoragePrefix",
  "indexedDbName",
  "proxyHeader",
  "tempFilePrefix",
  "wailsVersion",
];

function parseBrandYaml(text) {
  const brand = {};
  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }
    const match = trimmed.match(/^([a-zA-Z][a-zA-Z0-9]*):\s*(.*)$/);
    if (!match) {
      continue;
    }
    let value = match[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    brand[match[1]] = value;
  }
  return brand;
}

function goString(value) {
  return JSON.stringify(value);
}

