function syncConfigYml(brand) {
  const text = fs.readFileSync(CONFIG_YML, "utf8");
  const lines = text.split("\n");
  const infoKeys = {
    companyName: brand.company,
    productName: brand.displayName,
    productIdentifier: brand.bundleId,
    description: brand.description,
    copyright: brand.copyright,
    comments: brand.comments,
    version: brand.version,
  };

  let inInfo = false;
  let infoIndent = "";
  const out = [];

  for (const line of lines) {
    const infoMatch = line.match(/^(\s*)info:\s*$/);
    if (infoMatch) {
      inInfo = true;
      infoIndent = infoMatch[1];
      out.push(line);
      for (const [key, value] of Object.entries(infoKeys)) {
        out.push(`${infoIndent}  ${key}: ${JSON.stringify(value)}`);
      }
      continue;
    }
    if (inInfo) {
      if (/^\S/.test(line) && !line.startsWith(" ")) {
        inInfo = false;
        out.push(line);
        continue;
      }
      if (/^\s+\w+:/.test(line)) {
        continue;
      }
    }
    out.push(line);
  }

  fs.writeFileSync(CONFIG_YML, out.join("\n"));
}

function syncMsixManifest(brand) {
  if (!fs.existsSync(MSIX_MANIFEST)) {
    return;
  }
  const versionParts = brand.version.split(".").map((part) => part.padStart(1, "0"));
  while (versionParts.length < 4) {
    versionParts.push("0");
  }
  const msixVersion = versionParts.slice(0, 4).join(".");

  let text = fs.readFileSync(MSIX_MANIFEST, "utf8");
  text = text.replace(/Name="[^"]+"/, `Name="${brand.bundleId}"`);
  text = text.replace(/Version="[^"]+"/, `Version="${msixVersion}"`);
  text = text.replace(
    /<DisplayName>[^<]+<\/DisplayName>/,
    `<DisplayName>${brand.displayName}</DisplayName>`,
  );
  text = text.replace(
    /<PublisherDisplayName>[^<]+<\/PublisherDisplayName>/,
    `<PublisherDisplayName>${brand.company}</PublisherDisplayName>`,
  );
  text = text.replace(
    /<Description>[^<]+<\/Description>/g,
    `<Description>${brand.description}</Description>`,
  );
  text = text.replace(/<Application Id="[^"]+"/, `<Application Id="${brand.bundleId}"`);
  text = text.replace(/Executable="[^"]+"/g, `Executable="${brand.slug}"`);
  text = text.replace(
    /Description="A renbrowser application"/,
    `Description="${brand.description}"`,
  );
  text = text.replace(/<uap:VisualElements[\s\S]*?DisplayName="[^"]+"/, (block) =>
    block.replace(/DisplayName="[^"]+"/, `DisplayName="${brand.displayName}"`),
  );
  fs.writeFileSync(MSIX_MANIFEST, text);
}

function readTaskfileAppName() {
  const text = fs.readFileSync(TASKFILE, "utf8");
  const match = text.match(/^\s*APP_NAME:\s*"([^"]+)"/m);
  return match?.[1] ?? "";
}

function main() {
  const brandText = fs.readFileSync(BRAND_YML, "utf8");
  const brand = parseBrandYaml(brandText);

  for (const key of REQUIRED_KEYS) {
    if (!brand[key]) {
      console.error(`build/brand.yml: missing required key "${key}"`);
      process.exit(1);
    }
  }

  const appName = readTaskfileAppName();
  if (appName && appName !== brand.slug) {
    console.error(
      `build/brand.yml slug "${brand.slug}" does not match Taskfile.yml APP_NAME "${appName}"`,
    );
    process.exit(1);
  }

  fs.mkdirSync(path.dirname(GO_OUT), { recursive: true });
  fs.mkdirSync(path.dirname(TS_OUT), { recursive: true });

  fs.writeFileSync(GO_OUT, generateGo(brand));
  fs.writeFileSync(TS_OUT, generateTS(brand));
  syncConfigYml(brand);
  syncMsixManifest(brand);

  console.log("gen-brand: wrote internal/brand/brand_gen.go");
  console.log("gen-brand: wrote frontend/src/lib/brand.gen.ts");
  console.log("gen-brand: synced build/config.yml info section");
  console.log("gen-brand: synced build/windows/msix/app_manifest.xml");
}

main();
